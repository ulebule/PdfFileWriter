		/// <summary>
		/// Create type one font
		/// </summary>
		/// <param name="Document">Pdf document main object</param>
		/// <param name="FontCode">Type one font code</param>
		/// <returns>Font type1 object</returns>
		public static PdfFontTypeOne CreateFontTypeOne
				(
				PdfDocument Document,
				TypeOneFontCode FontCode
				)
			{
			// create array of fonts
			if(Document.TypeOneFont == null) Document.TypeOneFont = new List<PdfFontTypeOne>();

			// search the list for font
			int Index;
			for(Index = 0; Index < Document.TypeOneFont.Count && Document.TypeOneFont[Index].FontCode != FontCode; Index++);

			// font already defined
			if(Index < Document.TypeOneFont.Count) return Document.TypeOneFont[Index];

			// create a new font
			PdfFontTypeOne NewFont = new PdfFontTypeOne(Document, FontCode);
			Document.TypeOneFont.Add(NewFont);

			// return value
			return NewFont;
			}

		private PdfFontTypeOne
				(
				PdfDocument Document,
				TypeOneFontCode FontCode
				) : base(Document, ObjectType.Dictionary, "/Font")
			{
			// save font code and font info
			this.FontCode = FontCode;
			FontInfo = PdfFontTypeOne.TypeOneFontInfo[(int) FontCode];

			// add items to dictionary
			Dictionary.Add("/Subtype", "/Type1");
			Dictionary.Add("/BaseFont", "/" + TypeOneFontInfo[(int) FontCode].FontName);

			// create two resource codes
			ResourceCode = Document.GenerateResourceNumber('F');
			return;
			}

